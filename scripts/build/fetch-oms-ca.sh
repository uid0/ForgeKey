#!/usr/bin/env bash
# Refresh src/security/oms_ca.h with the live OMS HTTPS root certificate.
#
# Usage:
#     scripts/build/fetch-oms-ca.sh                     # uses oms.openmakerspace.org:443
#     scripts/build/fetch-oms-ca.sh staging.example:443
#
# What this does:
#   1. Opens a TLS connection to the host and dumps the full chain.
#   2. Extracts the root certificate (last cert in the chain).
#   3. Rewrites src/security/oms_ca.h with the PEM string baked in.
#
# Run this whenever OMS rotates its TLS chain. After running, rebuild +
# reflash devices before the back-end starts serving the new chain.

set -euo pipefail

HOST_PORT="${1:-oms.openmakerspace.org:443}"
HOST="${HOST_PORT%%:*}"

REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
OUT="$REPO_ROOT/src/security/oms_ca.h"

if ! command -v openssl >/dev/null; then
    echo "openssl is required" >&2
    exit 1
fi

echo "Fetching TLS chain from $HOST_PORT..."
CHAIN="$(openssl s_client -connect "$HOST_PORT" -servername "$HOST" \
    -showcerts </dev/null 2>/dev/null \
    | awk '/-----BEGIN CERTIFICATE-----/,/-----END CERTIFICATE-----/')"

if [ -z "$CHAIN" ]; then
    echo "Could not retrieve any certificates from $HOST_PORT" >&2
    exit 1
fi

# Last cert in the chain is the root (or the highest cert the server hands us).
ROOT_PEM="$(echo "$CHAIN" | awk '
    /-----BEGIN CERTIFICATE-----/ { buf = ""; in_cert = 1 }
    in_cert { buf = buf $0 "\n" }
    /-----END CERTIFICATE-----/ { last = buf; in_cert = 0 }
    END { printf "%s", last }
')"

if [ -z "$ROOT_PEM" ]; then
    echo "Failed to isolate root cert from chain" >&2
    exit 1
fi

# Emit the C header with the PEM as a multi-line C string literal.
{
    echo "#ifndef OMS_CA_H"
    echo "#define OMS_CA_H"
    echo
    echo "// Auto-generated by scripts/build/fetch-oms-ca.sh."
    echo "// Source: $HOST_PORT (fetched $(date -u +%Y-%m-%dT%H:%M:%SZ))"
    echo "// Re-run the script to refresh after OMS rotates its TLS chain."
    echo
    echo "#ifndef OMS_CA_PEM"
    echo "#define OMS_CA_PEM \\"
    echo "$ROOT_PEM" | sed 's/.*/"&\\n" \\/' | sed '$s/ \\$//'
    echo "#endif"
    echo
    echo "extern const char* kOmsCaPem;"
    echo
    echo "#endif"
} > "$OUT"

echo "Wrote $OUT"
