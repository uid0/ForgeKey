#!/usr/bin/env bash
# Generate a fresh ECDSA P-256 keypair for OTA firmware signing.
#
# Writes:
#   firmware-signing.key    private key (PKCS8 PEM) — keep secret, deploy as
#                           FORGEKEY_FIRMWARE_SIGNING_KEY on the OMS server
#   firmware-signing.pub    public key (PEM) — copy into
#                           src/security/firmware_pubkey.h before flashing
#
# After running, also bake the public key into the device firmware:
#     scripts/build/gen-firmware-signing-key.sh --update-header
# which rewrites src/security/firmware_pubkey.h in place.
#
# Old devices flashed with the previous public key will refuse OTAs signed
# by the new private key, so plan rotations carefully (typically: dual-sign
# binaries during the transition by baking both keys into the firmware).

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
KEY_DIR="$REPO_ROOT/.firmware-keys"
mkdir -p "$KEY_DIR"

PRIV="$KEY_DIR/firmware-signing.key"
PUB="$KEY_DIR/firmware-signing.pub"

if [ -f "$PRIV" ] && [ "${1:-}" != "--update-header" ]; then
    echo "Refusing to overwrite existing $PRIV." >&2
    echo "Move or delete it first if you intend to rotate." >&2
    exit 1
fi

if [ "${1:-}" != "--update-header" ]; then
    echo "Generating P-256 keypair..."
    openssl ecparam -name prime256v1 -genkey -noout -out "$PRIV"
    chmod 600 "$PRIV"
    openssl ec -in "$PRIV" -pubout -out "$PUB"
    echo "Private key: $PRIV"
    echo "Public key:  $PUB"
fi

if [ ! -f "$PUB" ]; then
    echo "Public key $PUB does not exist — generate first." >&2
    exit 1
fi

if [ "${1:-}" = "--update-header" ]; then
    HDR="$REPO_ROOT/src/security/firmware_pubkey.h"
    {
        echo "#ifndef FIRMWARE_PUBKEY_H"
        echo "#define FIRMWARE_PUBKEY_H"
        echo
        echo "// Auto-generated by scripts/build/gen-firmware-signing-key.sh --update-header."
        echo "// Source: $PUB (updated $(date -u +%Y-%m-%dT%H:%M:%SZ))"
        echo
        echo "#ifndef FORGEKEY_FW_PUBKEY_PEM"
        echo "#define FORGEKEY_FW_PUBKEY_PEM \\"
        cat "$PUB" | sed 's/.*/"&\\n" \\/' | sed '$s/ \\$//'
        echo "#endif"
        echo
        echo "extern const char* kFirmwarePubKeyPem;"
        echo
        echo "#endif"
    } > "$HDR"
    echo "Updated $HDR"
fi
